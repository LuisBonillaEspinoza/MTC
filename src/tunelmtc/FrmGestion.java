    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if(txtNombres1.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese un Nombre Correcto");
        }
        else if(txtApellidos.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese un Apellido Correcto");
        }
        else{
        int estado;
        String dni = txtDNI1.getText();
        String nom = txtNombres1.getText();
        String ape = txtApellidos.getText();
        if (jRadioButton1.isSelected()) {
            estado = 1;
        } else {
            estado = 0;
        }
        Operario op = new Operario();
        op.ModificarOperario(nom, ape, dni, estado);
        JOptionPane.showMessageDialog(null, "Modificado Correctamente");
        FrmManual manual = new FrmManual();
        manual.setVisible(true);
        this.dispose();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

