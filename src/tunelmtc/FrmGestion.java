    private void tblListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListaMouseClicked
        // TODO add your handling code here: 
        int pos = tblLista.getSelectedRow();
        if (pos < -1) {
            JOptionPane.showMessageDialog(null, "Elija una Fila Correcta");
        } else {
            txtDNI1.setText(tblLista.getValueAt(pos, 0).toString());
            txtNombres1.setText(tblLista.getValueAt(pos, 1).toString());
            txtApellidos.setText(tblLista.getValueAt(pos, 2).toString());
            if (tblLista.getValueAt(pos, 3).toString().equals("1")) {
                jRadioButton1.setSelected(true);
            } else {
                jRadioButton2.setSelected(true);
            }
        }
    }//GEN-LAST:event_tblListaMouseClicked

